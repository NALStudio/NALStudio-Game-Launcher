using UnityEngine;

namespace NALStudio.Cache
{
	public static class Cache
	{
		private static string path = System.IO.Path.Combine(Application.persistentDataPath, "cache");
		private static bool dirCreated = false;
		public static string Path
		{
			get
			{
				if (!dirCreated && !System.IO.Directory.Exists(path))
				{
					System.IO.DirectoryInfo di = System.IO.Directory.CreateDirectory(path);
					di.Attributes |= System.IO.FileAttributes.Hidden;
				}
				return path;
			}
		}
	}
}
